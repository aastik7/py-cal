#Adddition
def add(a,b):
    answer = a+b
    print( str(a) + "+" + str(b) + "=" + str(answer) ) 

#Subtraction
def sub(a,b):
    answer = a-b
    print( str(a) + "-" + str(b) + "=" + str(answer))

#Division
